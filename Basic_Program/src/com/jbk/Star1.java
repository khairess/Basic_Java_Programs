package com.jbk;

public class Star1 {
	public static void main(String[] args) {
		for(int i=8;i>=1;i--)
		{
			System.out.println("");
			for(int j=1;j<=i;j++)
			{
				System.out.print("*");
			}
		}
		
		for(int l=1;l<=8;l++)
		{
			System.out.println("");
			for(int k=1;k<=l;k++)
			{
				System.out.print("*");
			}
		}
	}

}
